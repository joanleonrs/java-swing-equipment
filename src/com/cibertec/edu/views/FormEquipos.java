    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        boolean esValido = esFormularioValido();
        
        if (esValido == true) {
            int codigo = Integer.parseInt(txtCodigo.getText());
            String modelo = txtModelo.getText();
            String marca = txtMarca.getText();
            String categoria = txtCategoria.getText();
            boolean disponible = false;

            if(cboEstado.getSelectedIndex() == 0) {
                disponible = true;
            }

            Equipo equipoAIngresar = new Equipo(codigo, modelo, marca, categoria, disponible);
            agregarEquipo(equipoAIngresar);
            JOptionPane.showMessageDialog(null, "Equipo registrado \nexitosamente!");

            resetearFormulario();
            btnAgregar.setVisible(false);
            btnNuevo.setVisible(true);    
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed
    
