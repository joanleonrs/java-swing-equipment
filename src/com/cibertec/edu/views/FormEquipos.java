    private DefaultTableModel obtenerModeloInicial() {
        Object[]cabeceras = {"CODIGO", "MODELO", "MARCA", "CATEGORIA", "ESTADO"};
        DefaultTableModel modeloInicial = new DefaultTableModel(cabeceras, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        return modeloInicial;
    }
    
    private void refrescarTablaEquipos() {
    	
    	DefaultTableModel modeloInicial = obtenerModeloInicial();

        resultSet = equipoRepository.getEquipos();
        try {
            while (resultSet.next()) {
                Equipo e = new Equipo();
                e.setCodigo(resultSet.getInt("codigo"));
                e.setCategoria(resultSet.getString("categoria"));
                e.setMarca(resultSet.getString("marca"));
                e.setModelo(resultSet.getString("modelo"));
                e.setDisponible(resultSet.getBoolean("disponible"));
                llenarCelda(e, modeloInicial);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "OcurriÛ un error en el sistema, por favor contacte al Administrador! Error: " + ex.getMessage());
        }
        tableResultados.setModel(modeloInicial);
    }
    
    private DefaultTableModel llenarCelda(Equipo e, DefaultTableModel modelo) {
        String[]celda = {
                String.valueOf(e.getCodigo()),
                e.getModelo(),
                e.getMarca(),
                e.getCategoria(), 
                ((e.isDisponible() == true ) ? "Disponible" : "No Disponible")
            };
        modelo.addRow(celda);
        return modelo;
    }
    
    private void agregarEquipo(Equipo e) {
    	equipoRepository.addEquipo(
    			e.getCodigo(), 
    			e.getModelo(), 
    			e.getMarca(), 
    			e.getCategoria(), 
    			e.isDisponible());
    	refrescarTablaEquipos();
    }
    
    private void modificarEquipo(Equipo e) {
    	equipoRepository.updateEquipo(
    			e.getCodigo(), 
    			e.getModelo(), 
    			e.getMarca(), 
    			e.getCategoria(), 
    			e.isDisponible());
    	refrescarTablaEquipos();
    }
    
    private void buscarEquipo(int codigoEquipo) {
    	
    	DefaultTableModel nuevoModelo = obtenerModeloInicial();
        resultSet = equipoRepository.buscarEquipos(codigoEquipo);
        try {
            while (resultSet.next()) {
                Equipo e = new Equipo();
                e.setCodigo(resultSet.getInt("codigo"));
                e.setCategoria(resultSet.getString("categoria"));
                e.setMarca(resultSet.getString("marca"));
                e.setModelo(resultSet.getString("modelo"));
                e.setDisponible(resultSet.getBoolean("disponible"));
                llenarCelda(e, nuevoModelo);
            }
//	        JOptionPane.showMessageDialog(null, "No se encontraron resultados \npara el criterio de \nb˙squeda ingresado.");
        	
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "OcurriÛ un error en el sistema, por favor contacte al Administrador! Error: " + ex.getMessage());
        }
        tableResultados.setModel(nuevoModelo);
    }
    
    private int obtenerUltimoCodigo() {
    	int codigoInicial = 1000;
    	ResultSet rs = equipoRepository.getUltimoCodigo();
    	try {
			if (rs.next() && rs.getInt(1) > 0) {
				codigoInicial = rs.getInt(1);
			}
		} catch (SQLException e) {
			e.printStackTrace();
		}
    	return codigoInicial;
    }
    
    private void resetearFormulario(){
    	btnEliminar.setEnabled(false);
    	btnModificar.setEnabled(false);
        txtCodigo.setText("");
        txtModelo.setText("");
        txtMarca.setText("");
        txtCategoria.setText("");
        cboEstado.setSelectedIndex(1);
        txtCodigo.requestFocus();
        txtCodigo.setText(obtenerNuevoCodigo());
        txtCriterioBusqueda.setText("");
    };
    
    private boolean esFormularioValido() {
        
        boolean esValido = false;
    
        if(txtModelo.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Por favor ingrese un modelo!");
            txtModelo.setText("");
            txtModelo.requestFocus();
        } else if (txtMarca.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese una marca!");
            txtMarca.setText("");
            txtMarca.requestFocus();
        } else if (txtCategoria.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese una categor√≠a!");
            txtCategoria.setText("");
            txtCategoria.requestFocus();
        } else {
            esValido = true;
        }
        
        return esValido;
    }
    
